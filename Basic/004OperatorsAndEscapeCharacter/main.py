




a = 10
b = 3

print("a + b ==> {}".format(a + b))       # 加法
print("a // b ==> {}".format(a // b))      # 整除
print("a > b ==> {}".format(a > b))       # 比较
print("a & b ==> {}".format(a & b))       # 位与
print('a is b ==> {}'.format(a is b))      # 身份判断
print("'a' in 'cat' ==> {}".format('a' in 'cat'))# 成员判断




